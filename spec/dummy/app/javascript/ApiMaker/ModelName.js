export default class ModelName {
  constructor(data) {
    this.data = data
  }

  human(args) {
    if (!args)
      args = {count: 1}

    if (args.count > 1 || args.count < 0) {
      var countKey = "other"
    } else {
      var countKey = "one"
    }

    var key = `activerecord.models.${this.data.modelClassData.paramKey}.${countKey}`
    return I18n.t(key)
  }
}

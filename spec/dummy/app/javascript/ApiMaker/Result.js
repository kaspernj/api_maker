export default class Result {
  constructor(data) {
    this.data = data
  }

  currentPage() {
    return this.data.response.current_page
  }

  models() {
    return this.data.models
  }

  totalCount() {
    return this.data.response.total_count
  }

  totalPages() {
    return this.data.response.total_pages
  }
}
